    private void loadDefNames(){
        EquFetcher equ = new EquFetcher();
        String[] s = equ.getDefNames();
        System.out.println("length" +s.length);
        for(int i = 0 ; i < s.length ; i++ ){
            model.setValueAt(s[i], i, model.getColumnCount()-1);
        }
    }
    
    private void populateTable(){
        String tmp;
        String hex;
        String[] rowData;
        for (int i = 0; i < 16; i++) {
            tmp = ( "0" + Integer.toHexString(i).toUpperCase()   );
            if(map.containsKey(tmp)){
                rowData = new String[4];
                hex = map.get(tmp);
                rowData[0] = tmp;
                rowData[1] = hex;
                rowData[2] = String.valueOf( Integer.parseInt(hex, 16) );
                rowData[3] = Integer.toBinaryString(Integer.parseInt(hex, 16));
                model.addRow(rowData);
            }
        }
        for (int i = 16; i < 128; i++) { //128
            tmp = ( Integer.toHexString(i).toUpperCase()   );
            if(map.containsKey(tmp)){
                rowData = new String[4];
                hex = map.get(tmp);
                rowData[0] = tmp;
                rowData[1] = hex;
                rowData[2] = String.valueOf( Integer.parseInt(hex, 16) );
                rowData[3] = Integer.toBinaryString(Integer.parseInt(hex, 16));
                model.addRow(rowData);
            }
        }
    }
    
    
    
    
